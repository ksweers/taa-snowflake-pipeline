# ---------------------------------------------------------------------------
# 4 & 9. Task DDL (full load and delta)
# ---------------------------------------------------------------------------

def gen_task_ddl(cfg: dict, pipeline: str) -> str:
    """
    pipeline = 'FL' (full load) or 'DL' (delta load)
    """
    tname      = cfg["table_name"].upper()
    wave       = cfg.get("wave", 1)
    parent_tbl = cfg.get("wave2_parent", "").upper() if wave == 2 else None
    wh         = cfg.get("warehouse", "WH_DS_AUTOMATION_TST")

    prefix     = f"TAA_{pipeline}"
    wave_label = f"W{wave}"
    task_name  = f"{prefix}_{wave_label}_{tname}"
    dispatcher = "FULL_LOAD_FROM_CONFIG" if pipeline == "FL" else "DELTA_LOAD_FROM_CONFIG"

    if wave == 1:
        after_clause = f"AFTER {prefix}_ROOT"
    else:
        after_clause = f"AFTER {prefix}_W1_{parent_tbl}"

    pipe_label = "full load" if pipeline == "FL" else "delta load"
    return f"""
CREATE OR REPLACE TASK {task_name}
    WAREHOUSE = {wh}
    COMMENT   = 'TAA {pipe_label} Wave {wave}: loads {tname}.'
    {after_clause}
AS
CALL {dispatcher}('{tname}');
"""


