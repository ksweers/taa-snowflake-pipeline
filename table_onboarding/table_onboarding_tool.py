def indent(text: str, spaces: int = 4) -> str:
    pad = " " * spaces
    return "\n".join(pad + line for line in text.splitlines())


def section(title: str) -> str:
    bar = "=" * 77
    return f"""
-- {bar}
-- {title}
-- {bar}
"""


def subsection(title: str) -> str:
    bar = "-" * 77
    return f"""
-- {bar}
-- {title}
-- {bar}
"""


# ---------------------------------------------------------------------------
# 1. Target table DDL
# ---------------------------------------------------------------------------

def gen_target_table(cfg: dict) -> str:
    tname = cfg["table_name"].upper()
    lines = []
    for col in cfg["columns"]:
        name = col["name"].upper()
        sf_type = col["sf_type"]
        nullable = " NOT NULL" if col.get("not_null", True) else ""
        lines.append(f"    {name:<50} {sf_type}{nullable},")

    # Primary key constraint
    pk_cols = [c["name"].upper() for c in cfg["columns"] if c.get("pk")]
    if pk_cols:
        pk_list = ", ".join(pk_cols)
        lines.append(f"    CONSTRAINT PK_{tname} PRIMARY KEY ({pk_list})")
    else:
        # Remove trailing comma from last column line
        if lines:
            lines[-1] = lines[-1].rstrip(",")

    col_block = "\n".join(lines)
    return f"""CREATE OR REPLACE TABLE {tname} (
{col_block}
);
"""


# ---------------------------------------------------------------------------
# 2. FULL_LOAD_<TABLE> procedure
# ---------------------------------------------------------------------------

def gen_full_load_proc(cfg: dict) -> str:
    tname  = cfg["table_name"].upper()
    tid    = cfg["table_uuid"]
    mt     = cfg.get("is_multi_tenant", True)

    # Build column list for COPY INTO target
    copy_cols = []
    select_exprs = []
    for col in cfg["columns"]:
        cname = col["name"].upper()
        copy_cols.append(f"                    {cname}")
        if mt and cname == "DATABASEPHYSICALNAME":
            # Derived from file path, not Parquet payload
            select_exprs.append(
                "                    REGEXP_SUBSTR(METADATA$FILENAME::STRING,"
                " ''/([^/]+)/Tables/'', 1, 1, ''e'')"
            )
        else:
            cast = parquet_cast(col["sf_type"])
            select_exprs.append(f"                    $1:{col['name']}{cast}")

    copy_col_block   = ",\n".join(copy_cols)
    select_expr_block = ",\n".join(select_exprs)

    return f"""{subsection(f"PROCEDURE: FULL_LOAD_{tname}")}
CREATE OR REPLACE PROCEDURE FULL_LOAD_{tname}(
    STAGE_NAME VARCHAR
)
RETURNS VARCHAR
LANGUAGE JAVASCRIPT
EXECUTE AS CALLER
AS '
    var total_rows_loaded     = 0;
    var files_processed       = 0;
    var total_files_identified = 0;

    try {{
        var get_files_query = `
            SELECT
                SUBSTRING(full_file_path, POSITION(''\\'/LandingZone/\\'' IN full_file_path)) AS relative_path,
                client_id,
                table_id,
                filename,
                full_file_path
            FROM STAGE_TAA_FULL_FILE_MANIFEST
            WHERE table_id = ''{tid}''
            ORDER BY client_id, table_id
        `;

        var file_results  = snowflake.createStatement({{sqlText: get_files_query}}).execute();
        var file_list     = [];
        var file_metadata = {{}};

        while (file_results.next()) {{
            var relative_path  = file_results.getColumnValue(1);
            var client_id      = file_results.getColumnValue(2);
            var table_id       = file_results.getColumnValue(3);
            var filename       = file_results.getColumnValue(4);
            var full_file_path = file_results.getColumnValue(5);

            file_list.push("''" + relative_path + "''");
            file_metadata[relative_path] = {{
                client_id:      client_id,
                table_id:       table_id,
                filename:       filename,
                full_file_path: full_file_path
            }};
        }}

        total_files_identified = file_list.length;
        if (total_files_identified === 0) {{
            return "No files found to load for {tname}.";
        }}

        var batch_size  = 1000;
        var batch_count = Math.ceil(file_list.length / batch_size);

        for (var batch_num = 0; batch_num < batch_count; batch_num++) {{
            var start        = batch_num * batch_size;
            var end          = Math.min(start + batch_size, file_list.length);
            var batch_files  = file_list.slice(start, end);
            var files_clause = batch_files.join(", ");

            var copy_command = `
                COPY INTO {tname} (
{copy_col_block}
                )
                FROM (
                    SELECT
{select_expr_block}
                    FROM @` + STAGE_NAME + `
                    (FILE_FORMAT => ''FF_TAA_ONELAKE_PARQUET'')
                )
                FILE_FORMAT = (TYPE = PARQUET)
                FILES = (` + files_clause + `)
                ON_ERROR = CONTINUE
                FORCE = TRUE
            `;

            var copy_result = snowflake.createStatement({{sqlText: copy_command}}).execute();

            while (copy_result.next()) {{
                var file_name   = copy_result.getColumnValue(1);
                var status      = copy_result.getColumnValue(2);
                var rows_loaded = copy_result.getColumnValue(4);
                var first_error = copy_result.getColumnValue(7);

                var relative_path = file_name.indexOf("/LandingZone/") > -1
                    ? file_name.substring(file_name.indexOf("/LandingZone/"))
                    : file_name;

                var metadata    = file_metadata[relative_path] || {{}};
                var load_status = (status === "LOADED") ? "SUCCESS" : "FAILED";

                var safe_filename  = (metadata.filename       || file_name).replace(/''/g, "''''");
                var safe_client_id = (metadata.client_id      || "UNKNOWN");
                var safe_table_id  = (metadata.table_id       || "UNKNOWN");
                var safe_full_path = (metadata.full_file_path || file_name).replace(/''/g, "''''");
                var safe_error     = first_error ? first_error.replace(/''/g, "''''") : null;

                snowflake.createStatement({{ sqlText: `
                    INSERT INTO INGEST_TAA_FILE_AUDIT
                        (file_name, client_id, table_id, rows_loaded, batch_number,
                         load_status, error_message, full_stage_path)
                    VALUES (
                        ''` + safe_filename  + `'',
                        ''` + safe_client_id + `'',
                        ''` + safe_table_id  + `'',
                        `  + rows_loaded     + `,
                        `  + (batch_num + 1) + `,
                        ''` + load_status    + `'',
                        `  + (safe_error ? "''" + safe_error + "''" : "NULL") + `,
                        ''` + safe_full_path + `''
                    )
                `}}).execute();

                total_rows_loaded += rows_loaded;
                files_processed++;
            }}
        }}

        return "Load complete. Processed " + batch_count + " batch(es). " +
               "Files: " + files_processed + " / " + total_files_identified + ". " +
               "Rows loaded: " + total_rows_loaded;

    }} catch (err) {{
        throw new Error("FULL_LOAD_{tname} failed: " + err.message);
    }}
';
"""


