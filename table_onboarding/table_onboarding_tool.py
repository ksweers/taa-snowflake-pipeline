def main():
    parser = argparse.ArgumentParser(
        description="Generate TAA pipeline onboarding SQL for a new table."
    )
    parser.add_argument("config", help="Path to the table config JSON file.")
    parser.add_argument(
        "--output", "-o",
        help="Output .sql file path. Defaults to onboard_<TABLE_NAME>.sql in the same directory as the config."
    )
    args = parser.parse_args()

    config_path = args.config
    if not os.path.exists(config_path):
        print(f"ERROR: Config file not found: {config_path}", file=sys.stderr)
        sys.exit(1)

    with open(config_path, "r", encoding="utf-8") as f:
        cfg = json.load(f)

    # Basic validation
    required = ["table_name", "table_uuid", "columns"]
    for field in required:
        if field not in cfg:
            print(f"ERROR: Missing required config field: '{field}'", file=sys.stderr)
            sys.exit(1)

    if not cfg["columns"]:
        print("ERROR: 'columns' list is empty.", file=sys.stderr)
        sys.exit(1)

    pk_cols = [c for c in cfg["columns"] if c.get("pk")]
    if not pk_cols:
        print("WARNING: No columns marked as pk=true. MERGE ON clause will be empty.", file=sys.stderr)

    sql = generate(cfg)

    if args.output:
        out_path = args.output
    else:
        config_dir = os.path.dirname(os.path.abspath(config_path))
        out_path   = os.path.join(config_dir, f"onboard_{cfg['table_name'].upper()}.sql")

    with open(out_path, "w", encoding="utf-8") as f:
        f.write(sql)

    print(f"Generated: {out_path}")
    print(f"  Table:   {cfg['table_name'].upper()}")
    print(f"  UUID:    {cfg['table_uuid']}")
    print(f"  Columns: {len(cfg['columns'])}")
    print(f"  Wave:    {cfg.get('wave', 1)}")
    print(f"  Multi-tenant: {cfg.get('is_multi_tenant', True)}")


if __name__ == "__main__":
    main()
