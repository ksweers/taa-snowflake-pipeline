# ---------------------------------------------------------------------------
# Snowflake type → Parquet COPY cast
# Used in FULL_LOAD_<TABLE> for the $1:ColName::TYPE expressions.
# ---------------------------------------------------------------------------
def parquet_cast(sf_type: str) -> str:
    t = sf_type.upper().strip()
    if t.startswith("VARCHAR") or t == "TEXT":
        return "::TEXT"
    if t.startswith("NUMBER") or t.startswith("NUMERIC") or t.startswith("DECIMAL"):
        return f"::{sf_type}"
    if t in ("BOOLEAN", "BOOL"):
        return "::BOOLEAN"
    if "TIMESTAMP_NTZ" in t:
        return "::TIMESTAMP_NTZ"
    if "TIMESTAMP_TZ" in t:
        return "::TIMESTAMP_TZ"
    if "TIMESTAMP_LTZ" in t:
        return "::TIMESTAMP_LTZ"
    if "TIMESTAMP" in t:
        return "::TIMESTAMP_NTZ"
    if t.startswith("FLOAT") or t.startswith("DOUBLE") or t == "REAL":
        return f"::{sf_type}"
    if t.startswith("DATE"):
        return "::DATE"
    if t.startswith("TIME"):
        return "::TIME"
    # Default: cast to the declared type
    return f"::{sf_type}"


# ---------------------------------------------------------------------------
# Snowflake type → CSV COPY cast (TRY_ wrappers for date/time types)
# Used in DELTA_LOAD_<TABLE> for the $N::TYPE positional expressions.
# ---------------------------------------------------------------------------
def csv_cast(sf_type: str) -> str:
    t = sf_type.upper().strip()
    if "TIMESTAMP" in t:
        return f"TRY_TO_TIMESTAMP_NTZ(${{pos}})"   # placeholder, filled in below
    if t.startswith("VARCHAR") or t == "TEXT":
        return "${{pos}}::TEXT"
    if t.startswith("NUMBER") or t.startswith("NUMERIC") or t.startswith("DECIMAL"):
        return f"${{pos}}::{sf_type}"
    if t in ("BOOLEAN", "BOOL"):
        return "${{pos}}::BOOLEAN"
    if t.startswith("FLOAT") or t.startswith("DOUBLE") or t == "REAL":
        return f"${{pos}}::{sf_type}"
    if t.startswith("DATE"):
        return "TRY_TO_DATE(${{pos}})"
    return f"${{pos}}::{sf_type}"


def csv_cast_expr(sf_type: str, pos: int) -> str:
    """Return the final CSV cast expression for a given column position."""
    t = sf_type.upper().strip()
    if "TIMESTAMP" in t:
        return f"TRY_TO_TIMESTAMP_NTZ(${pos})"
    if t.startswith("DATE"):
        return f"TRY_TO_DATE(${pos})"
    if t.startswith("VARCHAR") or t == "TEXT":
        return f"${pos}::TEXT"
    if t in ("BOOLEAN", "BOOL"):
        return f"${pos}::BOOLEAN"
    # NUMBER, FLOAT, etc.
    return f"${pos}::{sf_type}"


